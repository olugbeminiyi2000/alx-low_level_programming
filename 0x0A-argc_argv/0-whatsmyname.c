#include <stdio.h>
/*Write a program that prints its name, followed by a new line.*/
/**
 * main - Our main function
 *
 * @argc: first parameter
 * @argv: second parameter
 * Return: returns an int
 */
int main(__attribute__((unused)) int argc, char *argv[])
{
	int i = 0;

	printf("%s\n", argv[i]);
	return (0);
}
